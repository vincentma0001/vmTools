#define  __VM_WINERR_MAX_BUF__ 4096
#endif

/////////////////////////////////////////////////////////////////////////////////////////
// namespace 
namespace vm{

	/////////////////////////////////////////////////////////////////////////////////////
	//
	// class define : CWinError
	//        Notes : 此类封装了window错误代码及错误信息的获取操作
	//
	/////////////////////////////////////////////////////////////////////////////////////
	class CWinErr
	{
	/////////////////////////////////////////////////////////////////////////////////////////
	// members :
	private:
		unsigned long			mdwErrorCode;						// 错误代码
		std::string				mstrErrorDes;						// 错误代码描述
		static unsigned char	mszBuf[__VM_WINERR_MAX_BUF__];		// 缓存
	/////////////////////////////////////////////////////////////////////////////////////
	// functions :
	public:
		CWinErr(const unsigned long cdwError = GetLastError());     // Construct define
		~CWinErr(){};			                                    // Destruct define
	/////////////////////////////////////////////////////////////////////////////////////////
	// static functions:
	public:
		// 获取错误描述
		inline const char* ToString();
		// 获取错误码
		unsigned long ToCode();
		// 格式化错误输出
		const char* Format(const char* const szFormat);

	}; // End of class CWinError
	/////////////////////////////////////////////////////////////////////////////////////

}; // End of namespace vm
