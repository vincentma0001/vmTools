//                  windows 错误代码定义
//-----------------------------------------------------------------------------------
//   位  ：     | 31 - 30  |    29     |  28  |       27 - 16       |         15 -0
//-----------------------------------------------------------------------------------
// 二进制：     |   00     |     0     |  0   |  0000 0000 0000     | 0000 0000 0000 0000
//-----------------------------------------------------------------------------------
//                 严重性  | 系统/用户 | 保留 |      设备代码       |        异常代码
//                 -------------------------------------------------------------------
//                 0=成功  |  0=系统   |  0   |      系统定义       |      系统/客户定义
//                 1=供参考|  1=客户   |      |                     |
//                 2=警告  |           |      |                     |
//                 3=错误  |           |      |                     |
/////////////////////////////////////////////////////////////////////////////////////////
// 十六进制表示：
//    30-31 严重性：   成  功：0x00000000
//                     供参考：0x40000000
//                     警  告：0x80000000
//                     错  误：0xc0000000
//    29    系  统:    系  统：0x00000000
//                     客  户：0x20000000
