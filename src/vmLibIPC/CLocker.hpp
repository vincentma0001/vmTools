template< typename tMutex >
class CLocker
{
/////////////////////////////////////////////////////////////////////////////////////////
// Construct && Destruct
public:
    // Construct define
    explicit CLocker( tMutex& oMutex ):mMutex(oMutex) { mMutex.Enter();};
    // Destruct define
    virtual ~CLocker(){ mMutex.Leave();};
    
private:
    // No Copy
    CLocker(const CLocker& obj){};
    // No Assignment
    CLocker& operator = ( const CLocker& obj ){};

public:
    tMutex& mMutex;
    
}; // End of class CLocker
