// class release 
bool DoAction1(int argc, char *argv[])
{
	// Todo: do action 1
	std::cout << "This is action 1!" << std::endl;
	return true;
}

bool DoAction2(int argc, char *argv[])
{
	// Todo: do action 2
	std::cout << "This is action 2!" << std::endl;
	std::cout << std::string("This is a test!").c_str() << std::endl;
	return true;
}


/////////////////////////////////////////////////////////////////////////////////////////
// Todo : add application
void main(void)
{
	std::cout << "This is a test for tst of vmConsole" << std::endl;

	// initialize console object
	vm::vmConsole loConsole;

	// regist action function
	loConsole.RegCase("Action1", "this is action 1", (vm::vmConsole::PACTFUNC)DoAction1);
	loConsole.RegCase("Action2", "this is action 2", (vm::vmConsole::PACTFUNC)DoAction2);

	// run console
	loConsole.Run();

	::system("pause");
}


